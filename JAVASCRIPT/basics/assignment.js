// Fundamentals: variables, arithmetic, assignment operators, and output.
//
// - let: declares a block-scoped variable.
// - Arithmetic: +, -, *, / operate on numbers.
// - Assignment: = assigns a value; compound assignments (+=, -=, *=, /=) update a variable using an operator.
// - console.log: prints values to the developer console for debugging/inspection.

// simple arithmetic expression stored in `score`
let score = 10 + 11; // 10 + 11 evaluated to 21 and assigned to score

// declare a numeric variable
let num1 = 10; // initial value is 10

// Examples of assignment vs compound assignment:
// num1 = num1 + 5; // adds 5 to num1 using the long form
// Compound assignment does the operation and assignment in one step:
// Here we divide num1 by 5 and store the result back in num1.
num1 /= 5; // equivalent to: num1 = num1 / 5

// Output the final value of num1 to the console.
// For num1 = 10 and num1 /= 5, this logs 2.
console.log(num1);

